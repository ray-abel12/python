# or, and, ==, *, +, -,
int_a = 13
int_b = 15
int_c = 15
# and(|) operator 
if int_a < int_b and int_a == int_c :
    print("greater")
else:
    print("int b is greater")

# OR &

if int_a < int_b or int_a == int_c :
    print("greater")
else:
    print("int b is greater")

# equalto (==)

if int_a == int_b:
    print("equal to")
else:
    print("not equal to")
# *
result = int_a * int_b
print(result)

# +
result = int_a + int_b
print(result)

# -
result = int_a - int_b
print(result)
